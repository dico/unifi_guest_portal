<div style="margin:0 auto; max-width:500px;">
	<h1>Legg til enhet</h1>
	<p>
		Denne funksjonen er ment for å legge til enheter som ikke kan logge på gjestenettet med brukernavn og/eller passord.<br />
		Eksempelvis Apple TV, skrivere eller andre devicer. Kan også brukes på datamaskiner, men 
	</p>


	<h2>Beta (Test)</h2>
	<p>
		Dette er en test-funksjon inntil videre. Vi forbeholder oss retten til å fjerne funksjonen eller enheter som legges inn dersom vi
		oppdager problemer.
	</p>

	<p>
		Det kan også være tidsbegrensning på tilkoblinger som gjøres.
	</p>

	<h2>Steg</h2>
	<p>
		<b>1. Autentiser hvem du er med brukernavn og passord.</b><br />
		<i>Kan logge på med bruker i både LOGIN- og SKOLE-domenet.</i>


		<br /><br />
		<b>2. Velg sted</b><br />
		<i>
			Velg sted / hvilket trådløstnett enheten skal autorisere seg mot.
		</i>

		<br /><br />
		<b>3. Skriv inn MAC-adressen til enheten.</b><br />
		<i>
			MAC-adressen er en hardware-adresse som ofte står på en klisterlapp på enheten.<br />
			Eventuelt kan man finne den under nettverksinnstillinger, dersom man har mulighet til å vise dette på enheten (Apple TV, skriver, osv...).<br />
			Eksempler på format: 00:20:18:61:f1:8a eller 00-20-18-61-f1-8a.
		</i>


		<br /><br />
		<b>4. Skriv en kommentar.</b><br />
		<i>Skriv en kommentar på hva det er du har lagt inn.</i>
	</p>


